package firstcup.model;


public class AgeDifferenceInfo {
    private Long totalAgeDifference;
    private Long totalCount;

    public Long getTotalAgeDifference() {
        return totalAgeDifference;
    }

    public void setTotalAgeDifference(Long totalAgeDifference) {
        this.totalAgeDifference = totalAgeDifference;
    }

    public Long getTotalCount() {
        return totalCount;
    }

    public void setTotalCount(Long totalCount) {
        this.totalCount = totalCount;
    }
}
